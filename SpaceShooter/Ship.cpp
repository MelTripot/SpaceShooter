#include <stdio.h>
#include <SFML/Graphics.hpp>
#include <iostream>
#include <Ship.h>

using namespace std;
using namespace sf;

Ship::Ship(){}
Ship::Ship( float r,int x, int y){        
    sf::Sprite shape;
    sf::Texture text;
    if (!text.loadFromFile("Asset/playerShip1_blue.png"))
        {
            // not found
        }
    shape.setTexture(text);
    shape.setOrigin(50,38);
    shape.setPosition(x,y);
    shape.setRotation(r);
    }
    
void Ship::ShipRotation (int rotation){
    r += rotation;
    shape.setRotation(r);
}
    
